?>
<dl>
                    <dt class="first" id="what4chan">What is 4chan?</dt>
                    <dd>
                      <p>4chan is a simple image-based bulletin board where anyone can
                      post comments and share images. 4chan's collaborative-community format
                      was inspired by one of the most popular forums in Japan, Futaba Channel.
                      Different boards are dedicated to different topics, from Japanese anime,
                      manga, and culture to videogames, music, and photography. Users do not
                      need to register an account before participating in the community.</p>
                    </dd>

                    <dt id="howaccess">How do I access the boards?</dt>
                    <dd>
                      <p>In order to access the full site, you must first read and
                      agree to the terms of the disclaimer. To access the disclaimer
                      simply click any board and a dialogue will appear. Your response will be
                      cookied so you'll only need to do this once (providing you don't clear your
                      cache/cookies afterwards). If you wish to browse the site using frames, visit
                      /<a href="/frames">frames</a>.</p>
                    </dd>

                    <dt id="whatbasics">What should I know before I post?</dt>
                    <dd>
                      <p>Users are encouraged to participate actively in our community,
                      but only after they have familiarized themselves with the <a
                      href="/rules">rules</a> and this FAQ. Breaking the rules may
                      result in post deletion, a temporary ban, or in some cases,
                      permanent banishment. <em>Understand the rules before you begin
                      posting!</em></p>
                    </dd>

                    <dt id="postanon">How do I post anonymously?</dt>
                    <dd>
                      <p>To post as "Anonymous", simply do not fill in the [Name] field
                      when submitting content. Information such as your personal IP
                      address is viewable only to the administrators, and is not made
                      publicly available.</p>
                    </dd>

                    <dt id="register">Can I register a username?</dt>
                    <dd>
                      <p>No user registration process is available. If you're worried
                      about somebody impersonating you, consider using a <a
                      href="#tripcode">tripcode</a> to help validate your identity.</p>
                    </dd>

                    <dt id="postimage">Must I post an image?</dt>
                    <dd>
                      <p>To start a new thread, you must upload an image. 4chan
                      <em>is</em> primarily an <strong>image</strong>board after all!
                      When replying to an already existing thread, no attachment is
                      required.</p>
                    </dd>

                    <dt id="replyimage">Can I reply with an image?</dt>
                    <dd>
                      <p>Yes. To reply to a thread with an image of your own, click the
                      [Reply] link and fill in the post box as you normally would, making
                      sure to specify an image for attachment.</p>
                    </dd>

                    <dt id="quote">How do I quote somebody?</dt>
                    <dd>
                      <p>To quote a portion of text, simply place a pointer ("&gt;") in
                      front of the text you wish to highlight (ex. "<span
                      style="color:#789922">&gt;This is a quote</span>"). To link to and
                      highlight an entire post, place two pointers in front of its unique
                      post number (ex. "<span
                      style="color:#000080">&gt;&gt;210981</span>"). Cross-linking to
                      another 4chan board is also possible, by placing three pointers
                      before a board letter, followed by a post number (ex. "<span
                      style="color:#000080">&gt;&gt;&gt;/x/1208196</span>").</p><br />
                      <p>If you're using our <a href="#extension">inline extension</a>, simply
                      click the post number you'd like to reference and it will bring up a Quick Reply
                      window for you to write your response in. Highlighting text and then clicking the
                      post number will quote the selected text.</p>
                    </dd>

                    <dt id="spoiler">Can I mark a submission as a spoiler?</dt>
                    <dd>
                      <p>Only certain boards allow you to mask plot-spoiling content. To
                      mark your image as a spoiler, check the [x Spoiler Image?] box
                      before submission. Spoilerizing text makes it unreadable to others
                      until they mouse over it. To spoilerize a comment, place [spoiler]
                      tags around the text you wish to hide (ex. "[spoiler]SPIKE
                      DIES![/spoiler]"). Misuse of these tags may result in a ban.</p>
                    </dd>

                    <dt id="trip">How do I use a "tripcode"?</dt>
                    <dd>
                      <p>Tripcodes can help verify a user's identity to others, and are a
                      type of <em>pseudo</em>-registration. To use a normal tripcode,
                      place a hash mark ("#") followed by a word or short phrase after
                      what you've entered into the [Name] field (ex. "User#password").
                      Upon submission, the server will generate the hash unique to that
                      particular word or phrase. The previous example would display
                      "<span style="color:#000080"><strong>User</strong>
                      !ozOtJW9BFA</span>" after being posted.</p><br />
                      <p><strong>Important note:</strong> A tripcode is generated only
                      using the text entered after the hash mark. Your entered name, IP
                      address, cookie information, etc. do not affect the output. Normal
                      tripcodes are <em>not</em> secure, and can be cracked with relative ease.
                      For a more secure solution, see <a href="#sectrip">secure
                      tripcodes</a>.</p>
                    </dd>

                    <dt id="sectrip">What is a "secure tripcode"?</dt>
                    <dd>
                      <p>A <em>secure</em> tripcode can be generated by placing two
                      hash marks in the [Name] field, as opposed to one as with a
                      <em>normal</em> tripcode (ex. "User##password"). Secure tripcodes
                      use a secret key file on the server to help obscure their password.
                      The previous example would display "<span
                      style="color:#000080"><strong>User</strong> !!rEkSWzi2+mz</span>"
                      after being posted.</p>
                    </dd>

                    <dt id="capcode">What is a capcode?</dt>
                    <dd>
                      <p>A capcode is a way of verifying someone as a 4chan team member.
                      Normal users do not have the ability to post using a capcode. A <a
                      href="#whomod">moderator</a> capcode will display "<span
                      style="color:#800080"><strong>## Mod
                      <img src="//s.4cdn.org/image/modicon.gif" alt="Mod Icon" title="This user is a 4chan Moderator." style="margin-bottom: -3px;" /></strong></span>"
                      after their name. Similarly, the <a href="#whoadmin">administrator</a> capcode
                      displays "<span style="color:#FF0000"><strong>## Admin <img src="//s.4cdn.org/image/adminicon.gif" alt="Admin Icon" title="This user is the 4chan Administrator." style="margin-bottom: -3px;" /></strong></span>".
                      Often times, a moderator will post as
                      "<span style="color:#800080"><strong>Anonymous ##
                      Mod <img src="//s.4cdn.org/image/modicon.gif" alt="Mod Icon" title="This user is a 4chan Moderator." style="margin-bottom: -3px;" /></strong></span>" in order to keep their identity secret (which
                      is policy). <a href="#whojan">Janitors</a> do not receive a
                      capcode.</p><br />
                      <p><strong>Note:</strong> Icons will always appear alongside a capcode, and will display a tooltip that says "This user is a/the 4chan Moderator/Administrator" upon mousing over them. If there is no icon/tooltip, the poster is
                      an impersonator.</p>
                    </dd>

                    <dt id="sage">What is "sage"?</dt>
                    <dd>
                      <p>Entering "sage" (by itself) into the [Options] field while
                      replying will cause the thread not to bump to the top of the page. 
                      Contrary to popular belief, a sage is not a downvote, and should not be used as one. "sage-bombing" or 
                      announcing that you've saged a thread may result in a ban.</p>
                    </dd>

                    <dt id="nonoko">How can I be returned to the board index after I post?</dt>
                    <dd>
                      <p>In August 2012 the default behavior of posting was changed. Upon posting users 
                      are redirected to their post instead of the board index. To enable the legacy behavior of being 
                      returned to the board index after posting, enter "nonoko" into the [Options] field.</p>
                    </dd>

                    <dt id="nonokosage">Can I use nonoko and sage at the same time?</dt>
                    <dd>
                      <p>Yes, enter "nonokosage" into the [Options] field.</p>
                    </dd>

                    <dt id="report">How do I report posts?</dt>
                    <dd>
                      <p>Reporting posts for review is extremely encouraged, as it helps us remove rule breaking and/or illegal content faster. To file a report, click the "&#9654;" button to the right of the post number to expand the post menu, and then select the "Report post" option. A confirmation dialogue will pop up and ask if the post you are reporting is a rule violation, spam, or if it contains illegal material. Be sure to mark this appropriately! Fill out the captcha, and then click on the Submit button.</p><br />
                      <p><strong>Important note:</strong> Submitting intentionally false reports may result in a ban. This policy is in place to discourage abuse of the system; it is not meant to dissuade users from submitting legitimate reports.</p>
                    </dd>

                    <dt id="delete">How do I delete my own post(s)?</dt>
                    <dd>
                      <p>When you submit a post, a password cookie is automatically set that will 
                      allow you to delete your post should you choose to. As long as your browser retains this cookie, you
                      will be able to delete posts made using it. To delete your post(s), check the box in the upper
                      lefthand corner of the specific post(s) you wish to remove, and
                      scroll down to the bottom of the page and click the [Delete]
                      button on the right. Checking the [x File Only] box will cause only the image to be
                      deleted, leaving the comment untouched. Note that some boards and threads
                      (ex. stickies) do not allow a user to delete their posts.</p>
                    </dd>

                    <dt id="bump">Why won't my thread bump?</dt>
                    <dd>
                      <p>All threads have a set bump limit (varies board to board). When
                      this limit is reached, a thread will no longer "bump" to the top of
                      the board, causing the thread to descend through the pages until it
                      is bumped off of the last page and pruned. This method of post-limiting,
                      while sometimes inconvenient, assures that content is kept fresh on
                      the boards.</p>
                    </dd>

                    <dt id="worksafe">What does "work safe" mean?</dt>
                    <dd>
                      <p>"Work safe" is defined as "content that is safe for viewing in
                      the average working environment." Boards that default to the Yotsuba B or
                      Burichan (blue) theme are to be considered work safe. Posting
                      content that is not suitable for this environment may result in a
                      ban. The goal behind enforcing this rule is to keep the cleaner
                      sections of the site accessible to those in restricted viewing
                      situations.</p>
                    </dd>

                    <dt id="prunedelete">My post disappeared! Where'd it go?</dt>
                    <dd>
                      <p>Threads expire and are pruned by 4chan's software at a
                      relatively high rate. Since most boards are limited to ten
                      pages, content is usually available for only a few hours or days before
                      it is removed. Most times, the post was probably pruned automatically, however in some cases it
                      may have been removed by a moderator or janitor.
                      We ask that users refrain from reposting material that they believe
                      to have been deleted by the moderators.</p>
                    </dd>

                    <dt id="archive">Can I retrieve an old post/image?</dt>
                    <dd>
                      <p>No. Content that has expired is removed from our servers. Please do not contact us requesting
                      content that has been pruned, for it is impossible for us to assist
                      you. Instead, try placing a request on the /<a
                      href="//boards.4chan.org/r/" title="Request">r</a>/
                      board.</p>
                    </dd>

                    <dt id="usercontact">Can you help me contact a specific user?</dt>
                    <dd>
                      <p>No. Unless a user elects to include an e-mail address or other contact information in their
                      post, there is no way to contact that user privately. More details regarding our storage of personal
                      information can be found <a href="#personalinfo">here</a>.</p>
                    </dd>

                    <dt id="imagesource">How can I find the source of an image?</dt>
                    <dd class="last">
                      <p>Google Image Search and iqdb are great resources to identify the source of an image. 
                        You can access these services using our inline extension by clicking the "&#9654;" button to the right of a post containing an image, and then selecting "Image search". Remember: Posting requests for source information on the boards may get you banned&mdash;use /<a href="//boards.4chan.org/r/" title="Requests" target="_blank">r</a>/ for all requests.</p>
                    </dd>
                  </dl>
